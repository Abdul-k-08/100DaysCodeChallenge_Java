package Day3.Operators;

public class Day3_Assignment {
    public static void main(String[] args) {
        int a  = 40; // Assign
        System.out.println(a); //40

        a += 20; //Add and Assign
        System.out.println(a); //40 + 20 = 60

        a-=10; //Subtract and Assign
        System.out.println(a); //60 - 10 = 50

        a*=2; //Multiply and Assign
        System.out.println(a); //50 * 2 = 100

        a/=2; //Divide and Assign
        System.out.println(a); //100 / 2 = 50

        a%=2; //Modulo and Assign
        System.out.println(a); //50 % 2 = 0 --> 2*25=50, Remainder 0
    }
}
