package Day3.Operators;

public class Day3_Arithmatic {
    public static void main(String[] args) {
        int a= 20;
        int b= 7;

        System.out.println("A ➕ B = " + (a+b)); //Addition

        System.out.println("A ➖ B = " + (a-b)); // Subtraction

        System.out.println("A ✖️ B = " + (a*b)); // Multiplication

        System.out.println("A ➗ B = "+ ((double)a/b)); // Division

        System.out.println("A % B = "+ (a%b) ); // Module --> Remainder
    }
}
